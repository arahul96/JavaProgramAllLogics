package wrapperclasses;

public class Wrapperclass3 {

	public static void main(String[] args)
	{
		//AutoBoxing process
		Integer ob1 = 12;
		Float ob2 = 12.34F;
		Character ob3 = 'A';
		Boolean ob4 = true;
		
		System.out.println("Value is: "+ob1.toString());
		System.out.println("Value is: "+ob2.toString());	
		System.out.println("Value is: "+ob3.toString());	
		System.out.println("Value is: "+ob4.toString());
	}
}
